    private double GetDishPrice (String dish) {
        double price = 0.0;
        
        try {
            rs = statement.executeQuery("select m.price from Menu m where dishname='"
                            + dish + "'");

            // this stores the price of the selected dish automatically
            if (rs.next()) {
                price = Double.parseDouble(rs.getString(1));
            } else {
                price = 0.0;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return price;
    }
    
