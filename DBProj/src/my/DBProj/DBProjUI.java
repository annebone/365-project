    private int GetRestaurantID (String name) {
        int ret = 0;
        
        try {
            rs = statement.executeQuery("select r.restaurant_id from Restaurants r where r.name = '" + name + "'");
            
            if (rs.next()) {
                ret = Integer.parseInt(rs.getString(1));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return ret;
    }
    
    private String[] GetRestaurantNames () {
        String[] names = new String[10];
        int count = 0;
        
        try {
            rs = statement.executeQuery("select count(r.name) from Restaurants r");
            
            if (rs.next()) {
                names = new String[Integer.parseInt(rs.getString(1))];
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        try {
            rs = statement.executeQuery("select r.name from Restaurants r");
        
            while (rs.next()) {
                names[count] = rs.getString(1);
                count++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return names;
    }
    
